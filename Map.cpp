Map::Map() {

	dirt = TextureManager::LoadTexture("assets/pamant100.png");
	grass = TextureManager::LoadTexture("assets/iarba100.png");
	water = TextureManager::LoadTexture("assets/apa100.png");

	//	trebuie verificat daca s-a incarcat corect harta
	loadMap(lvl_1);

	src.x = src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;
	dest.x = dest.y = 0;
}

Map::~Map() {

	SDL_DestroyTexture(dirt);
	SDL_DestroyTexture(grass);
	SDL_DestroyTexture(water);
}

void Map::loadMap(int arr[20][25]) {

	for (int row = 0; row < 20; row++) {
		for (int col = 0; col < 25; col++) {
			map[row][col] = arr[row][col];
		}
	}
}

void Map::drawMap() {

	int tipTeren = 0;

	for (int row = 0; row < 20; row++) {
		for (int col = 0; col < 25; col++) {

			tipTeren = map[row][col];

			dest.x = col * 32.f;
			dest.y = row * 32.f;

			switch (tipTeren)
			{
			case 0:
				TextureManager::Draw(water, src, dest);
				break;
			case 1:
				TextureManager::Draw(grass, src, dest);
				break;
			case 2:
				TextureManager::Draw(dirt, src, dest);
				break;
			default:
				break;
			}
		}
	}
}